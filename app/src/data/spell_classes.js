function norm(s) {
  return (s || '').toString().toLowerCase()
    .replace(/[''`]/g, '').replace(/[àá]/g, 'a').replace(/[èé]/g, 'e')
    .replace(/[ìí]/g, 'i').replace(/[òó]/g, 'o').replace(/[ùú]/g, 'u')
    .replace(/\s+/g, '_').replace(/[^a-z0-9_]/g, '')
}

// Fallback: deduce le classi probabili dalla scuola dell'incantesimo
// (usato quando lo spell non è nella mappa esplicita).
function classesFromScuola(scuola) {
  if (!scuola) return ['mago']
  const lower = scuola.toLowerCase()
  if (lower.includes('abiurazione')) return ['chierico', 'mago', 'paladino']
  if (lower.includes('ammaliamento')) return ['bardo', 'mago', 'stregone', 'warlock']
  if (lower.includes('divinazione')) return ['bardo', 'chierico', 'mago']
  if (lower.includes('evocazione')) return ['chierico', 'druido', 'mago']
  if (lower.includes('illusione')) return ['bardo', 'mago', 'stregone']
  if (lower.includes('invocazione')) return ['mago', 'stregone', 'warlock']
  if (lower.includes('necromanzia')) return ['chierico', 'mago', 'warlock']
  if (lower.includes('trasmutazione')) return ['druido', 'mago']
  return ['mago']
}

export function getSpellClasses(spell) {
  if (!spell) return []
  const k = norm(spell.name)
  if (SPELL_CLASS_MAP[k]) return SPELL_CLASS_MAP[k]
  return classesFromScuola(spell.scuola)
}
